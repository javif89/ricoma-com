<p class="heading">
    What are cookies?
</p>
<p class="">
    A cookie is a small file containing a string of characters that is sent to your computer when you visit a website. When
    you visit the site again, the cookie allows that site to recognize your browser. Cookies may store user preferences and
    other information. <br class="">
    
    Cookies provide a convenience feature to save you time, or tell the Web server that you have returned to a specific
    page. For example, if you personalize pages on our Websites, a cookie helps us to recall your specific information on
    subsequent visits. When you return to the same Website, the information you previously provided can be retrieved, so you
    can easily use the customized features. <br class="">
    
    Cookies set by the website owner (in this case, Ricoma) are called "first party cookies". Cookies set by parties other
    than the website owner are called "third party cookies". Third party cookies enable third party features or
    functionality to be provided on or through the website (e.g. like advertising, interactive content and analytics). The
    parties that set these third party cookies can recognize your computer both when it visits the website in question and
    also when it visits certain other websites.
</p>

